// --- TOAST NOTIFICATIONS ---
export const ToastContainer: FC<{ toasts: ToastMessage[] }> = ({ toasts }) => {
  return (
    <div className="fixed bottom-5 right-5 z-50 space-y-3 w-80">
      {toasts.map((toast) => (
        <Toast key={toast.id} message={toast.message} type={toast.type} />
      ))}
    </div>
  );
};

const Toast: FC<Omit<ToastMessage, 'id'>> = ({ message, type }) => {
  const baseClasses = "p-4 rounded-md shadow-lg flex items-center text-white text-sm";
  const typeClasses = {
    success: "bg-green-500",
    error: "bg-red-500",
    info: "bg-blue-500",
  };

  return (
    <div className={`${baseClasses} ${typeClasses[type]}`}>
      <p>{message}</p>
    </div>
  );
};

